
!            *********************************************************************************
!            * "Write & Read & Variables Types .F90" is a little program for learning write  * 
!            *  and read commands and the way to determine the type of the variables.        * 
!            *********************************************************************************


program writeread

implicit none

!**********************************************************************************************************************
!                                       Determine variables
!**********************************************************************************************************************
character(len=20)   nam   ,w  ! nam*20

integer       i    ,j     ,k

real*8        p    ,q     ,r     ,a     ,b     ,n         

complex*16    Ii 

logical       x    ,y

!**********************************************************************************************************************
!                                         Zero to variables
!**********************************************************************************************************************

i=0    ;j=0     ;k=0

p=0.   ;q=0.    ;r=0.

Ii=(0.,1.)

x=.true. ;y=.false.


!**********************************************************************************************************************
!                                          printe results     
!**********************************************************************************************************************

write(*,*)  ! free line

nam='Mostafa'
write(*,*)nam

write(*,*)                                        i,j,k  ,p,q,r  ,Ii  ,x,y 

!  Using '(  )' instead second *

write(*,'(a,/)')'^^^^^^^^^^^^^^^^^^^^^^^^^^^^^^^^^^^^^^^^^^^^^^^^^^^^^^^^^^^^^^^^^^^'

write(*,'(3i4,3f7.3,2f7.3,a,a,/)')                i,j,k  ,p,q,r  ,Ii  ,x,y 
write(*,'(a,/)')'-------------------------------------------------------------------' ! on line

write(*,'(10x,a,i3,14x,a,i3,14x,a,i3,/)')        'i = ',i,'j = ',j,'k = ',k  
write(*,'(a,/)')'___________________________________________________________________' ! under line

write(*,'(10x,a,f7.3,10x,a,f7.3,10x,a,f7.3,/)')  'p = ',p,'q = ',q,'r = ',r  
write(*,'(a,/)')'==================================================================='

write(*,'(a,/)')'              Real Part  Imaginary Part'
write(*,'(10x,a,f7.3,4x,f7.3,/)')                'i = ',Ii  
write(*,'(a,/)')'...................................................................'

write(*,'(10x,a,l,15x,a,l,/)')                       'x = ',x,'y = ',y 
write(*,'(a,/,/,/)')'"""""""""""""""""""""""""""""""""""""""""""""""""""""""""""""""""""' 
 
 read(*,*) !Stop Run Program
!------------------------------------------------

write(*,*)
write(*,*)'***********************************************'
write(*,*)'*                                             *'
write(*,*)'*        |j,m> = <m1,m2|j,m> |m1,m2>          *'
write(*,*)'*                                             *'
write(*,*)'***********************************************'
write(*,*)

!------------------------------------------------
write(*,*)
write(*,*)
write(*,*)'------------------------------- Simpson--------------------------------------'
write(*,*)Char(244),' X(2n)'
write(*,*)         '|                                                                   5     (4) '
write(*,*)         '|  f(x)dx = (h/3)[F1+F3+......+F(2n-1)] + [F2+F4+......+F(2n)] + (nh /90) F(',Char(238),')'
write(*,*)         '|'
write(*,*)Char(245),' X0'
write(*,*)

write(*,*)
write(*,'(a,\)')' Enter X0    :    '
read(*,*) a

write(*,*)
write(*,'(a,\)')' Enter X(2n) :    '
 read(*,*) b

write(*,*)
write(*,'(a,\)')' Enter n     :    '
 read(*,*) n
write(*,*)

write(*,*)
write(*,'(a,\)')' RESULT      = '
!write (*,*)r
write(*,*)
write(*,*)

write(*,*)'*******************************'
write(*,'(a,\)') ' Do you want to continue? (Y or N)  : ' 
 read(*,*)w

!------------------------------------------------
write(*,*)
write(*,*)
do i=0,255 
   write (*,*) i,Char(i)     !char(255) is nul.
end do

write(*,*)
write(*,*)

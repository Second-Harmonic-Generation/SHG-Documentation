write(*,'(a,/)')'*******************************************************************'

write(*,'(a,/)')'&&&&&&&&&&&&&&&&&&&&&&&&&&&&&&&&&&&&&&&&&&&&&&&&&&&&&&&&&&&&&&&&&&&'

write(*,'(a,/)')'???????????????????????????????????????????????????????????????????'

write(*,'(a,/)')':::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::'

write(*,'(a,/)')';;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;'

write(*,'(a,/)')'%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%'

write(*,'(a,/)')'$$$$$$$$$$$$$$$$$$$$$$$$$$$$$$$$$$$$$$$$$$$$$$$$$$$$$$$$$$$$$$$$$$$'

write(*,'(a,/)')'!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!'

write(*,'(a,/)')'<<<<<<<<<<<<<<<<<<<<<<<<<<<<<<<<<<<<<<<<<<<<<<<<<<<<<<<<<<<<<<<<<<<'

write(*,'(a,/)')'>>>>>>>>>>>>>>>>>>>>>>>>>>>>>>>>>>>>>>>>>>>>>>>>>>>>>>>>>>>>>>>>>>>'

write(*,'(a,/)')'|||||||||||||||||||||||||||||||||||||||||||||||||||||||||||||||||||'

write(*,'(a,/)')'~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~' 

write(*,'(a,/)')'###################################################################'           

